package ifelse;

public class UseElseIf {

	public static void main(String[] args) {
		
		int num = 90;
		
		if(num>=90){
			System.out.println("Grade is A");
		}else if(num>=80){
			System.out.println("Grade is B");
		}else if(num>=70){
			System.out.println("Grade is C");
		}else if(num>=60){
			System.out.println("Grade is D");
		}

	}

}
